namespace Beef.Events
{
    /// <summary>
    /// Defines the event string format.
    /// </summary>
    public enum EventStringFormat
    {
        /// <summary>
        /// No formatting; as-is (default).
        /// </summary>
        None,

        /// <summary>
        /// Format as upper case.
        /// </summary>
        Uppercase,

        /// <summary>
        /// Format as lower case.
        /// </summary>
        Lowercase
    }
}